fun printStarPattern()
{
    for(i in 1..5)
    {
        for(j in 1..i)
        {
            print('*')
        }
        println()
    }
}
fun main()
{
    //Q3rd
    /*
    Write a Kotlin program that defines a function printStarPattern() which prints the following pattern:
    *
    **
    ***
    ****
    *****
    */

    printStarPattern()
}