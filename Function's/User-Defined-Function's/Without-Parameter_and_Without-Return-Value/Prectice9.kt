fun printNumberPyramid()
{
    for(i in 1..5)
    {
        for(j in 1..i)
        {
            print(j)
        }
        println()
    }
}
fun main()
{
    //Q9th
    /*
    Write a Kotlin program that defines a function printNumberPyramid() which prints the following pattern:
    1
    12
    123
    1234
    12345
    */

    printNumberPyramid()
}